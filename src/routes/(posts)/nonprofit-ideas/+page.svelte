<h1>Ideas for the newly rich</h1>
<div class="opacity-60 mb-8">June 2026</div>
<p>
	With the AI labs about to IPO, we’ll have a lot of newly minted tech ~billionaires, many of whom
	feel like they got rich somewhat incidentally and don’t have much ego tied to it. This is an
	opportunity for some really interesting philanthropic pursuits to emerge. Here are some ambitious
	ideas that I’m personally excited about:
</p>

<h2>Hotel-like sleep lab</h2>
<p>
	Sleep is the most important thing for health, a huge fraction of people these days have sleep
	issues, and sleep labs today are terrible:
</p>
<ul>
	<li>
		It’s nearly impossible to book multiple nights, which is crazy because single-night results are
		corrupted by the first-night effect given how unusual the lab sleeping conditions are
	</li>
	<li>Results inexplicably take weeks to arrive</li>
	<li>You need a referral from a doctor, which serves no meaningful filtering purpose</li>
</ul>
<p>
	It would be amazing to have a super comfortable sleep lab that lets you book online, no doctor in
	the loop, allows you to book multiple nights in a row, sends you your results immediately so you
	can have AI agents analyze them. There could be multiple tiers of fanciness, the top one nice
	enough to be a good third date. Crucially, the lab’s data should be public, so anyone can compare
	their results against the full distribution of people who slept in the same conditions. Staying
	anonymous could be allowed, though discouraged; publishing the data itself would be
	non-negotiable.
</p>
<p>
	A reason this is hard to make viable as a business today is insurance. Insurance companies are not
	happy with skipping doctor referrals, booking multiple nights, or excess comfort. So this business
	would need to eschew insurance entirely, which usually means serving only the rich, but the gap can 
	be filled with philanthropic support.
</p>

<h2>Public data hospital</h2>
<p>
	A more brazen generalization of the public data sleep lab—a hospital that publicizes all of its
	data by default: every scan, every diagnosis, every doctor recommendation, every declined patient,
	every mistake. Protocols on GitHub. Ideally even video of every interaction.
</p>
<p>
	Patients can request their data be omitted, but they’re encouraged and maybe even incentivized to
	let it be public. This would fix a lot of the information asymmetry problems in healthcare and
	incentivize doctors to do what’s actually right, since eventually AI will be able to retroactively
	look at their decisions and evaluate whether they were a good doctor. Ideally, doctor referrals
	should not be required; video applications would be a better filter.
</p>
<p>
	Of course the privacy issue is a non-starter for many types of ailments, but that’s okay; this
	hospital doesn’t need to handle everything, it just does what it can. Hopefully this can also help
	normalize sharing health problems.
</p>

<h2>Incentive rating agency</h2>
<p>
	Most of the harm in the world comes not from bad people but from business models that reward
	negative-sum behavior. A rating agency that deeply understands good incentive design could become
	a trusted authority on evaluating any type of business with information asymmetry problems. It
	could rate a wide range of institutions—hospitals, schools, restaurants, mortgage brokers,
	startups, governments—or focus on one type. It would dig deep into how employees are promoted,
	dark patterns like making it hard to leave, pricing transparency, outcome accountability, and
	so on.
</p>
<p>
	The meta challenge is ensuring the rating agency itself retains good incentives. It helps to have
	radical transparency, to be funded by vetted donors rather than the rated agencies themselves, and
	maybe to have red teams that seek inaccurate ratings, but none of these are complete solutions. I
	think it ultimately rests on the founders’ willpower to keep enforcing purity top-down.
</p>

<h2>AGI short films</h2>
<p>
	People tend to believe the fate of the world is predetermined by the forces of capitalism or
	something, but it isn’t; public opinion is highly swayable, leaders are swayed by sufficiently
	strong public opinion, and a shared concrete vision can go far. The world is still lacking good
	drama that helps people understand AGI more than it misleads. A feature-length movie would be cooler
	than a short, but it’d probably take so long it’d be outdated before release. Money doesn’t
	directly solve the core challenge of combining good understanding of AGI with mastery of drama,
	but I think money can set up a system where these talents collide. The ideal structure for this
	may be something like a residency program for screenwriters paired with alignment researchers,
	backed with access to famous producers.
</p>
<p>
	There is of course the PR issue that no matter what narrative is presented, many will see these
	films as propaganda to boost lab equity value. I’m not sure whether this can be eliminated, but we
	can push back with Sloan-style independence, published funding, and a mandate to greenlight scenes
	that make labs look bad (e.g. regulatory capture, cutting corners under competitive pressure).
</p>

<hr />

<p>
	There are of course a lot of reasons why none of these things exist, and they’re not easily solved
	by having new smart billionaires. Many of them brush against deeply resistant industries and lead
	to countless lawsuits. They’re socially weird and controversial; they are not the easy social
	status boosts that most of philanthropy is.
</p>
<p>
	The reason I feel like there’s hope here is because many of these newly minted billionaires are
	weird. They don’t share the same social circles that past billionaires have had. Many inherit more
	of their temperament from academia than from business, many have frequented LessWrong, many apply
	social pressure on each other to be altruistic.
</p>
<p>
	Maybe more importantly, many young people who once dreamed of starting a startup are now finding
	that traditional startups are losing their meaning. AGI timelines are a major reason, but not the
	only one. In general, as startups have become socially legitimized, they no longer feel like the
	route for the crazy ones, the misfits, the rebels. They're now simply a standard career path. The
	unconventional-minded need a new frontier to direct their ambition at, and impossible nonprofits
	may be it.
</p>
